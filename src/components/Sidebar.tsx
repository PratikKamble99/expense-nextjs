export function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const { isOpen, close } = useSidebar();
  const [isLoggingOut, setIsLoggingOut] = useState(false);

  const handleLogout = async () => {
    setIsLoggingOut(true);
    await signOut();
    router.push("/auth/login");
  };

  const isActive = (href: string) => pathname === href;

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/40 z-40 md:hidden"
          onClick={close}
        />
      )}

      {/* Sidebar — fixed on all breakpoints, does not scroll with page */}
      <aside
        className={`
          fixed left-0 top-0 h-screen
          w-64 bg-surface-container-low
          border-r border-line-subtle/10
          flex flex-col
          transition-transform duration-300 ease-out
          z-40
          ${isOpen ? "translate-x-0" : "-translate-x-full md:translate-x-0"}
        `}
      >
        {/* Header */}
        <div className="px-6 py-6 border-b border-line-subtle/10 shrink-0">
          <h1 className="text-lg font-bold text-on-surface tracking-tight">
            Luminescent
          </h1>
          <p className="text-xs text-on-surface-variant mt-1">Ledger</p>
        </div>

        {/* Navigation — only this section scrolls if items overflow */}
        <nav className="flex-1 px-4 py-6 space-y-2 overflow-y-auto">
          {navItems.map((item) => {
            const active = isActive(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={close}
                className={`
                  flex items-center gap-3 px-4 py-3 rounded-xl
                  transition-all duration-200 ease-out
                  ${
                    active
                      ? "bg-primary/15 text-primary shadow-primary-glow"
                      : "text-on-surface-variant hover:bg-surface-container-high hover:text-on-surface"
                  }
                `}
              >
                {item.icon}
                <span className="font-medium text-sm">{item.label}</span>
              </Link>
            );
          })}
        </nav>

        {/* Footer */}
        <div className="px-4 py-4 border-t border-line-subtle/10 shrink-0">
          <button
            onClick={handleLogout}
            disabled={isLoggingOut}
            className="
              w-full flex items-center gap-3 px-4 py-3 rounded-xl
              text-on-surface-variant hover:bg-surface-container-high hover:text-on-surface
              transition-all duration-200 ease-out
              disabled:opacity-50 disabled:cursor-not-allowed
            "
          >
            <svg
              xmlns="http://www.w3.org/2000/svg"
              className="w-5 h-5"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
            >
              <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4" />
              <polyline points="16 17 21 12 16 7" />
              <line x1="21" x2="9" y1="12" y2="12" />
            </svg>
            <span className="font-medium text-sm">
              {isLoggingOut ? "Logging out..." : "Logout"}
            </span>
          </button>
        </div>
      </aside>
    </>
  );
}
